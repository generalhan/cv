.MODEL SMALL
.STACK 100H
.DATA
 GLOBAL FLEG:WORD
 GLOBAL CD:WORD
 GLOBAL MAS:WORD
 GLOBAL I:WORD
 GLOBAL PROBERKA_ZAKRITIE:PROC
.386
.CODE
;-----------------------------------------------------------------------------
PROBERKA_ZAKRITIE PROC
                      ;-------------------------------------------------------
                      CMP FLEG,0
                      JE NET_FLEG
                      ;------------ëéêíàêéÇäÄ DI-çÄ èÖêÇéÖ åÖëíé--------------
                      MOV CX,CD
                      SUB CX,I
                      CMP CX,0
                      JE NET_FLEG
                      ;-------------------------------------------------------
                      PUSH CX
                      LEA SI,MAS
                      IMUL CX,2
                      ADD SI,CX
                      POP CX
                      ;-------------------------------------------------------
                      CKL_CX:
                           MOV AX,[SI-2]
                           MOV [SI],AX
                           SUB SI,2
                      LOOP CKL_CX
                      LEA SI,MAS
                      MOV [SI],DI
                      ;-------------------------------------------------------
                      NET_FLEG:RET
PROBERKA_ZAKRITIE ENDP
