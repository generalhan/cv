.MODEL SMALL
.STACK 100H
.DATA
 GLOBAL MAS:WORD
 GLOBAL CD:WORD
 GLOBAL PROCEDURE_MAS:PROC
 PUBLIC DIRE
 DIRE DW 0
.386
.CODE
;-----------------------------------------------------------------------------
PROCEDURE_MAS PROC
             LEA SI,MAS
             MOV AX,CD
             IMUL AX,2
             ADD SI,AX
             SUB SI,2
             MOV DI,[SI] 
             MOV DIRE,DI
             RET
PROCEDURE_MAS ENDP
;-----------------------------------------------------------------------------
END
